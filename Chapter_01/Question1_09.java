package Chapter_01;

/*
 * (Area and perimeter of a rectangle) Write a program that displays the area and 
 *  perimeter of a rectangle with a width of 4.5 and a height of 7.9 using the
 *  following formula: 
 *      area = width * height
 */

public class Question1_09 {
    public static void main(String[] args) {
        System.out.println("Width = 4.5, Height = 7.9");
        System.out.println("Area is " + (4.5 * 7.9));
        System.out.println("Perimeter is " + (2 * (4.5 + 7.9)));
    }
}
